function buildRootTsx(events: VoiceContextEvent[]): string {
  const durationInFrames = Math.max(240, events.length * 90 + 120);
  return `import { Composition } from "remotion";
import { Replay } from "./Replay";
import events from "./data/events.json";

export type ReplayEvent = {
  schemaVersion: 1;
  id: string;
  at: string;
  source: string;
  kind: string;
  title: string;
  summary: string;
  cwd?: string;
  toolName?: string;
  command?: string;
  status?: string;
};

export const RemotionRoot = () => {
  return (
    <Composition
      id="CodexThreadReplay"
      component={Replay}
      durationInFrames={${durationInFrames}}
      fps={30}
      width={1920}
      height={1080}
      defaultProps={{
        events: events as ReplayEvent[],
      }}
    />
  );
};
`;
}

