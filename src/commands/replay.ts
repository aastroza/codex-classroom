import fs from "node:fs/promises";
import path from "node:path";

import type { CommandContext } from "../types.js";
import { CliError } from "../core/errors.js";
import { pathExists } from "../core/fs.js";
import { buildThreadBrief, readVoiceContextEvents, type VoiceContextEvent } from "../core/voice-context.js";

export async function replayCommand(context: CommandContext, args: string[]): Promise<void> {
  const action = args[0] ?? "help";

  if (action === "help" || action === "--help" || action === "-h") {
    printReplayHelp(context);
    return;
  }

  if (action === "export") {
    await exportReplay(context, args.slice(1));
    return;
  }

  throw new CliError(`Unknown replay command: ${action}`);
}

async function exportReplay(context: CommandContext, args: string[]): Promise<void> {
  const outputDir = path.resolve(args[0] ?? path.join(process.cwd(), "codex-thread-replay"));
  const events = await readVoiceContextEvents(context.paths.classroomRoot, 120);
  if (events.length === 0) {
    throw new CliError("No Codex Voice context events found. Run codex-classroom voice install-hook and record a thread first.");
  }

  if ((await pathExists(outputDir)) && !context.options.force) {
    throw new CliError(`Output directory already exists: ${outputDir}. Use --force to overwrite generated files.`);
  }

  await fs.mkdir(path.join(outputDir, "src", "data"), { recursive: true });
  await fs.writeFile(path.join(outputDir, "package.json"), `${JSON.stringify(buildPackageJson(), null, 2)}\n`, "utf8");
  await fs.writeFile(path.join(outputDir, "README.md"), buildReplayReadme(events), "utf8");
  await fs.writeFile(path.join(outputDir, "tsconfig.json"), buildTsconfig(), "utf8");
  await fs.writeFile(path.join(outputDir, "remotion.config.ts"), buildRemotionConfig(), "utf8");
  await fs.writeFile(path.join(outputDir, "src", "index.ts"), buildIndexTs(), "utf8");
  await fs.writeFile(path.join(outputDir, "src", "Root.tsx"), buildRootTsx(events), "utf8");
  await fs.writeFile(path.join(outputDir, "src", "Replay.tsx"), buildReplayTsx(), "utf8");
  await fs.writeFile(path.join(outputDir, "src", "data", "events.json"), `${JSON.stringify(events, null, 2)}\n`, "utf8");
  await fs.writeFile(path.join(outputDir, "src", "data", "brief.txt"), `${buildThreadBrief(events, 24)}\n`, "utf8");

  if (context.options.json) {
    context.output.json({ ok: true, outputDir, events: events.length });
    return;
  }

  context.output.info(`Exported Remotion replay project to ${outputDir}`);
  context.output.info(`Events: ${events.length}`);
  context.output.info("Next:");
  context.output.info(`  cd ${outputDir}`);
  context.output.info("  npm install");
  context.output.info("  npm run preview");
  context.output.info("  npm run render");
}

